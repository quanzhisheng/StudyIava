public class ReportTabLayout {

    private int id;

    private int reportId;

    private int reportTabId;

    private int itemId;

    private String itemName;

    private int itemSizeX;

    private int itemSizeY;

    private int itemRowSize;

    private int itemColSize;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getReportId() {
        return reportId;
    }

    public void setReportId(int reportId) {
        this.reportId = reportId;
    }

    public int getReportTabId() {
        return reportTabId;
    }

    public void setReportTabId(int reportTabId) {
        this.reportTabId = reportTabId;
    }

    public int getItemId() {
        return itemId;
    }

    public void setItemId(int itemId) {
        this.itemId = itemId;
    }

    public String getItemName() {
        return itemName;
    }

    public void setItemName(String itemName) {
        this.itemName = itemName;
    }

    public int getItemSizeX() {
        return itemSizeX;
    }

    public void setItemSizeX(int itemSizeX) {
        this.itemSizeX = itemSizeX;
    }

    public int getItemSizeY() {
        return itemSizeY;
    }

    public void setItemSizeY(int itemSizeY) {
        this.itemSizeY = itemSizeY;
    }

    public int getItemRowSize() {
        return itemRowSize;
    }

    public void setItemRowSize(int itemRowSize) {
        this.itemRowSize = itemRowSize;
    }

    public int getItemColSize() {
        return itemColSize;
    }

    public void setItemColSize(int itemColSize) {
        this.itemColSize = itemColSize;
    }
}
